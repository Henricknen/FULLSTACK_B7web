<?php
$numero = 0;        // variável '$numero' iniçiada em  0

while($numero < 10) {       // 'while 'enquanto a condição for sastisfeita
    echo "N: ". $numero. "<br>";

    $numero += 1;       // a cada execução do loop '$numero' será incrementado '+1' fazendo a variável '$numero' chegar a 10 que encerrará a repetição
}