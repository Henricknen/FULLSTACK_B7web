<?php

/*      comentário de multiplas linhas
* Função 'somar'
*
* Está classe vai pega um valor 'x' e soma com o valor 'y'
*
* @package ControleDeEstoque
* @author Luis Henrique Silva Ferreira <l.henrick@live.com>
*
* @param $x float É o primeiro valor a ser somado
* @param $y float É o segundo valor a ser somado
* @return float
*
*/

function somar($x, $y) {
    return $x + $y;
}

